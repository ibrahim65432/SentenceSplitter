public class SentenceBS {
    int beginning;
    int end;

    SentenceBS(int beginning, int end){
        this.beginning = beginning;
        this.end = end;
    }



    public int getBeginning() {
        return beginning;
    }

    public void setBeginning(int beginning) {
        this.beginning = beginning;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }
}
